const t = (key, language, fallbackLanguage = "en", replacements) => {
  let translationString = key; // Default to key if no translation found

  if (translations[language] && translations[language][key] !== undefined) {
    translationString = translations[language][key];
  } else if (
    translations[fallbackLanguage] &&
    translations[fallbackLanguage][key] !== undefined
  ) {
    translationString = translations[fallbackLanguage][key];
  }

  if (replacements) {
    Object.keys(replacements).forEach((placeholder) => {
      const regex = new RegExp(`{${placeholder}}`, "g");
      translationString = translationString.replace(
        regex,
        replacements[placeholder]
      );
    });
  }

  return translationString;
};

